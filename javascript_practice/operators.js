//== comparison operator
const PI=3.14;
if(PI=="3.14")
{
    console.log("it is a pi")
}
else
{
    console.log("it is not a pi")
}
//=== strict equality operator
const P=3.14;
if(P==="3.14")
{
    console.log("it is a pi")
}
else
{
    console.log("it is not a pi")
}
//inequality operator
const s=3.14;
if(s!="3.14")
{
    console.log("it is  not a pi")
}
else
{
    console.log("it is  a pi")
}
//stict inequality operator
const l=3.14;
if(l!=="3.14")
{
    console.log("it is a pi")
}
else
{
    console.log("it is not a pi")
}
